# function that calculates the area of a circle
def circle(radius):
    area = radius * 3.14
    return area

# function that calculates the area of a rectangle
def rectangle(side1, side2):
    area = side1 * side2
    return area

# function that calculates the area of a square
def square(side):
    area = side * side
    return area

# function that calculates the area of a triangle
def triangle(side1, side2):
    area = side1 * side2 * (0.5)
    return area
